int ft_is_prime(int nb);

int ft_is_prime(int nb)
{
    int i;
    
    i = 2;
    if (nb == 2 || nb == 3 || nb == 5 || nb == 7)
        return (1);
    if (nb < 2)
        return (0);
    while (i * i <= nb)
    {
        if (nb % i == 0) return (0);
        i ++;
    }
    return (1);

}
